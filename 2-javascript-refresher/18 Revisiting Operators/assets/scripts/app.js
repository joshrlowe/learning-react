console.log("Hello World!"); // Prints "Hello World!" to the console.
console.log("Hello " + "World!"); // Concatenates two strings and prints "Hello World!" to the console.
console.log(10 === "10"); // Strict equality check, returns false because data types are different.
console.log(10 === 10); // Strict equality check, returns true.
console.log(10 == 10); // Type-converting equality check, returns true.
console.log(10 > 5); // Checks if 10 is greater than 5, returns true.

